<h2>Hilfe / FAQ / Allgemeine Fragen zu FK </h2>

<h3>Was ist FKr?</h3>

<p>FK ist die beste M&ouml;glichkeit, Fotos online zu speichern, zu durchsuchen, zu sortieren und mit anderen zu teilen! FK hilft Ihnen beim Organisieren Ihrer riesigen Mengen an Fotos und bietet Ihnen, Ihren Freunden und Ihrer Familie die M&ouml;glichkeit, Geschichten zu diesen Fotos zu erz&auml;hlen.</p>

<p>Am besten k&ouml;nnen Sie FK kennenlernen, indem Sie ein paar Fotos hochladen, die Website erkunden, einigen Gruppen beitreten und ein paar Freunde erstellen.</p>



<h3>Welche Regeln gelten bei FK?</h3>


<p>Die folgenden Community-Richtlinien sollen Ihnen dabei helfen, zu verstehen, was es bedeutet, ein Mitglied von FK zu sein. Vergessen Sie nicht, dass Ihre Nutzung von FK diesen Richtlinien und unseren Allgemeinen Gesch&auml;ftsbedingungen unterliegt.</p>

<h3>Was Sie tun sollten</h3>

<p>Seien Sie freundlich und respektvoll.
Wir sind eine globale Community, die sich aus vielen unterschiedlichen Menschen zusammensetzt. Diese haben alle das Recht, sich wohl zu f&uuml;hlen, auch wenn sie nicht so denken wie Sie, nicht daran glauben, woran Sie glauben und m&ouml;glicherweise andere Ansichten haben als Sie. Seien Sie also im Umgang mit anderen Mitgliedern stets freundlich und respektvoll.</p>
<p>Laden Sie Inhalte hoch, die Sie erstellt haben.</p>
<p>Respektieren Sie das Urheberrecht anderer. Das hei&szlig;t, Sie d&uuml;rfen keine Fotos oder Videos stehlen, die andere freigegeben haben, und als eigene Inhalte ausgeben. (Hierf&uuml;r gibt es Favoriten.)</p>
<h3>Moderieren Sie Ihre Inhalte.</h3>
<p>Alle Inhalte auf FK, egal ob &ouml;ffentlich oder privat, m&uuml;ssen mithilfe unserer Sicherheits- und Inhaltsfilter angemessen als "unbedenklich", "mittel" <p>oder "eingeschr&auml;nkt" moderiert werden. Wenn sich Ihre Einsch&auml;tzung als unzureichend herausstellt, stufen wir Ihren Account entsprechend der richtigen Einstellung f&uuml;r die sichere Suche und/oder f&uuml;r den Inhaltstyp ein und senden Ihnen eine Verwarnung.
Setzen Sie einen Link zur&uuml;ck zu FK, wenn Sie Ihre FK Inhalte anderswo ver&ouml;ffentlichen.</p>
Mit FK k&ouml;nnen Sie auf FK gehostete Inhalte auch auf anderen Websites posten. Die Seiten anderer Websites, die auf fk.com gehostete Inhalte zeigen, m&uuml;ssen f&uuml;r jedes Foto oder Video einen R&uuml;cklink zur entsprechenden Seite auf FK enthalten. Dies ist eine M&ouml;glichkeit, weitere Informationen zum Inhalt und zum Fotografen zu erhalten.</p>
<p>Haben Sie Spa&szlig; bei FK!</p>
<p>Sehen Sie die Welt mit den Augen anderer, machen Sie mit, finden Sie Inspiration und erweitern Sie Ihren Horizont!</p>



<h3>Wird FK immer kostenlos sein?</h3>

<p>Ja, es wird immer eine kostenlose Version von FK geben, und deren einzige Beschr&auml;nkung ist das Speicherplatzlimit von 1 Terabyte. Sie k&ouml;nnen auf einen Doublr-Account aufr&uuml;sten, um 2 Terabyte Speicherplatz zu erhalten.</p>

Weitere Informationen finden Sie auf der Upgrade-Seite.


<h3>Ich w&uuml;rde gern ein Foto verwenden, das ich bei FK gefunden habe. Wie soll ich vorgehen?</h3>

<p>Unsere Mitglieder haben eine unglaubliche Anzahl toller Werke auf Fk ver&ouml;ffentlicht. Wenn es ein Bild gibt, das Sie gern verwenden m&ouml;chten, suchen Sie nach dem Link "Antrag auf Lizenzierung" neben der Lizenz auf der Fotoseite. Wir haben uns mit Getty Images zusammengetan, die das Bild &uuml;berpr&uuml;fen und festlegen, ob es sich zur Lizenzierung durch das Unternehmen eignet und die bei Bedarf alle Details ausarbeiten.</p>

<p>Dies ist nicht bei allen Mitgliedern aktiviert. Wenn Sie es nicht sehen, k&ouml;nnen Sie sich auch direkt an das Mitglied wenden. Als Mitglied bei FK k&ouml;nnen Sie Ihren Mauszeiger &uuml;ber das Buddy-Icon einer Person bewegen. Ein Klick auf den kleinen Pfeil &ouml;ffnet das Benutzermen&uuml;. W&auml;hlen Sie dann "Fkmail senden", und erstellen Sie Ihre Nachricht. Wenn Sie einen Fotografen kontaktieren, ist es hilfreich, wenn Sie so viele Informationen wie m&ouml;glich &uuml;ber das Foto und sich selbst angeben und beschreiben, wie Sie das Foto verwenden m&ouml;chten.</p>


